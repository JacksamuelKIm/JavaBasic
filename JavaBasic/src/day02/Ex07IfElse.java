package day02;
// If else의 구조는
// If의 조건식이  true가 나오면
// If의 코드 블락만 실행되고
// false가 나오면
// else의 코드 블락만 실행이 된다.

public class Ex07IfElse {

	public static void main(String[] args) {
		int age = 20;
		if (age >= 18) {
			System.out.println("성인입니다.");
		} else {
			System.out.println("미성년자입니다.");			
		}
		
		
	}// main end
	
}
